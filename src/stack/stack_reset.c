#include "stack.h"

void
stack_reset(int *stack[4])
{
	int		i;

	i = 0;
	stack[0][0] = stack[2][0];
	stack[1][0] = 0;
	while (++i <= stack[0][0])
		stack[0][i] = stack[2][i];
}
